# Write a Python program which takes three numbers and display total and
# average of those numbers

firstNumber = int(input("Please enter the first number: "))
secondNumber = int(input("Please enter the second number: "))
thirdNumber = int(input("Please enter the third number: "))

total = firstNumber + secondNumber + thirdNumber
average = (firstNumber + secondNumber + thirdNumber)/3

print("Total of those three numbers is:", total)
print("Average of those three numbers is: ", average)
print("Thank you for participating!")
